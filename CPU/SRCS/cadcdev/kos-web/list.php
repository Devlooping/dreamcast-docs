<p>If you have any further questions, <b>please</b> join the SourceForge
mailing list and ask there. You won't get laughed off for any question,
honest. :) The mailing list for KOS is cadcdev-kallistios. You can also
get a notice when a new Subversion push is ready by subscribing to the
list cadcdev-svn-commits.</p>

<p><a href="http://sourceforge.net/mail/?group_id=23791">List info can
be found here.</a></p>

<p>There are many DC-related forums around the net where people meet up and
chat about development using KOS. However, the definitive place to ask
questions and interact with the developers is with the mailing list above.
If you need a question answered, ask it there! That said, there are a
couple of central places people go to talk about DC related development:</p>

<p><ul>
<li><a href="http://www.dcemu.co.uk/">DCEmu.co.uk</a> -- This really seems to
be the primary place to go these days. It's pretty active and a lot of
users and developers frequent it.</li>

<li><a href="http://www.consolevision.com/">Consolevision</a> -- Not as active
anymore, but it's still a good place to go look at forum archives and such.</li>

<li><a href="http://www.dcemulation.com/">DCEmu</a> -- This is filled primarily
with users, so if you want some feedback or beta testing this can be a good
place to go. It's a little more focused on DC-based emulation, as the name
implies, but other stuff is discussed in the forums too.</li>

<li><a href="http://www.cagames.com/">Cryptic Allusion</a> -- The company
formed by the original author of KOS (Dan Potter) and a couple of others.
This is focused more on the games CA has created, but there's also an indie
game developers' forum that may be useful.</li>

</ul></p>

